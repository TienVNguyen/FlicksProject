package com.training.tiennguyen.flicksproject.itemDecoration;

import android.content.Context;
import android.graphics.Canvas;
import android.graphics.drawable.Drawable;
import android.support.v4.content.ContextCompat;
import android.support.v7.widget.RecyclerView;
import android.view.View;

import com.training.tiennguyen.flicksproject.R;

/**
 * {@link SimpleDividerItemDecoration}
 *
 * @author TienVNguyen
 */
public class SimpleDividerItemDecoration extends RecyclerView.ItemDecoration {
    /**
     * Drawable Divider
     */
    private Drawable mDivider;

    /**
     * Constructor
     *
     * @param context {@link Context}
     */
    public SimpleDividerItemDecoration(final Context context) {
        this.mDivider = ContextCompat.getDrawable(context, R.drawable.line_divider);
    }

    @Override
    public void onDraw(Canvas c, RecyclerView parent, RecyclerView.State state) {
        int left = parent.getPaddingLeft();
        int right = parent.getWidth() - parent.getPaddingRight();
        int childCount = parent.getChildCount();

        for (int i = 0; i < childCount; i++) {
            View child = parent.getChildAt(i);

            RecyclerView.LayoutParams params = (RecyclerView.LayoutParams) child.getLayoutParams();

            int top = child.getBottom() + params.bottomMargin;
            int bottom = top + mDivider.getIntrinsicHeight();

            mDivider.setBounds(left, top, right, bottom);
            mDivider.draw(c);
        }
    }
}
